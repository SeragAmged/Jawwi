package com.sergio.jawwi.presentation.viewmodel.navigation

/**
 * Enum class representing the different screens in the application.
 * Used for navigation and managing the current screen state.
 */
enum class Screen {
    /**
     * Represents the main home screen of the application.
     * Displays current weather and basic forecast information.
     */
    Home,

    /**
     * Represents the detailed forecast screen showing weather predictions
     * for the upcoming week.
     */
    NextWeekForecast
}
